package main.java.day2.calc;

public class Calc {

    protected int a, b;

    public Calc() {
    }

    public int add(int... args) {
        return 0;
    }

    public int add(int a, int b) {
        return a + b;
    }

    public int add(int a, int b, int c) {
        return a + b + c;
    }

    public int add(int a, int b, int c, int d) {
        return a + b + c + d;
    }

    public int add(int a, int b, int c, int d, int e) {
        return a + b + c + d + e;
    }
}
